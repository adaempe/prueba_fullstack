import mongoose from "mongoose";

const pokeSchema = new mongoose.Schema(
  {
    atributo: {
      type: String,
      required: true,
    },
    date: {
      type: Date,
      default: Date.now,
    },
    user: {
      type: mongoose.Types.ObjectId,
      ref: "User",
    },
  },
  {
    timestamps: true,
  }
);

export default mongoose.model("Poke", pokeSchema);